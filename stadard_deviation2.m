function [sdr sdg sdb]=stadard_deviation2(i)
v1=i(:,:,1);
v2=i(:,:,2);
v3=i(:,:,3);
[m n]=size(v1);
vr=(sum(v1));
vr=(sum(vr));
vg=sum(v2);
vg=sum(vg);
vb=sum(v3);
vb=sum(vb);
v1=uint32(v1);
v3=uint32(v3);
v2=uint32(v2);
v1=power(v1,7);
v2=power(v2,7);
v3=power(v3,7);
Vr=sum(v1);
Vr=sum(Vr);
Vg=sum(v2);
Vg=sum(Vg);
Vb=sum(v3);
Vb=sum(Vb);
n=n*m;
cr=((Vr/n)-(vr/n)*(vr/n));
sdr=nthroot(cr,7);
cg=((Vg/n)-(vg/n)*(vg/n));
sdg=nthroot(cg,7);
cb=((Vb/n)-(vb/n)*(vb/n));
sdb=nthroot(cb,7);